public class Car2 {
    private String Bmw;
    private String e60;
    private int d2009;

    public Car2(String Bmw, String e60, int d2009) {
        this.Bmw = Bmw;
        this.e60 = e60;
        this.d2009 = d2009;
    }

    public String getBmw() {
        return Bmw;
    }

    public void setBmw(String Bmw) {
        this.Bmw = Bmw;
    }

    public String getE60() {
        return e60;
    }

    public void setE60(String e60) {
        this.e60 = e60;
    }

    public int getD2009() {
        return d2009;
    }

    public void setD2009(int d2009) {
        this.d2009 = d2009;
    }
}
